/**
 * @file (auth)/_layout.tsx
 * @description 認証関連画面のレイアウト。
 *
 * ============================================================
 * 【なぜ "(auth)" と括弧で囲むのか — Route Groups の由来】
 * ============================================================
 *
 * ■ Route Groups（ルートグループ）とは:
 *   (auth) のように括弧で囲まれたディレクトリは「グループ」と呼ばれる。
 *   グループはURLパスに影響しない（URLに "auth" は含まれない）が、
 *   共通のレイアウトを適用できる。
 *
 * ■ なぜ括弧なのか:
 *   Next.js 13 の app/ ディレクトリで導入された規約。
 *   - 括弧なし: フォルダ名がURLの一部になる（例: auth/ → /auth/login）
 *   - 括弧あり: フォルダ名はURLに含まれない（例: (auth)/ → /login）
 *   → 括弧は「整理のためだけのフォルダ」という意味。
 *
 *   ファイルシステム上ではフォルダを分けて整理したいが、
 *   URL構造には影響させたくない場合に使う。
 *   例: 認証画面とメイン画面で別のレイアウト(Provider)を適用したいが、
 *       URLは /login, /home のようにフラットにしたい。
 *
 * ■ この規約の背景:
 *   Unix/Linux ではファイル名に括弧を使うことは稀なので、
 *   「特別な意味を持つファイル名」として視覚的にも目立つ。
 *   プログラミングの世界では「メタ情報」を括弧で表現する慣習がある
 *   （例: 数学の関数 f(x)、プログラミングの関数呼び出し func()）。
 *
 * ============================================================
 * 【ディレクトリ構造】
 * ============================================================
 *
 *   src/app/(auth)/_layout.tsx        → ★このファイル（認証グループのレイアウト）
 *   src/app/(auth)/index.tsx          → 認証グループのインデックス（ログインへリダイレクト）
 *   src/app/(auth)/login/index.tsx    → ログインページ
 *
 * 【Slot の役割】
 * <Slot /> は「子ルートをここに表示する」というプレースホルダー。
 * /(auth)/login にアクセスすると、この Slot の位置に Login コンポーネントが表示される。
 *
 * ■ "Slot" という名前の由来:
 *   英語の "slot"（差し込み口）。自動販売機のコイン投入口のイメージ。
 *   「ここに何かを差し込む場所」という意味で、Web Components の <slot> 要素、
 *   Vue.js の <slot>、Laravel Blade の @yield など、多くのフレームワークで使われる概念。
 *
 * 認証ガードはルートレイアウト（_layout.tsx）の useRouteGuard で処理されるため、
 * ここではシンプルに Slot を表示するのみ。
 */

