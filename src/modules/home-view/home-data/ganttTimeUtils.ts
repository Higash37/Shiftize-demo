/**
 * @file ganttTimeUtils.ts
 * @description ガントチャートで使う時間関連のユーティリティ。
 *   30分刻みの時間ラベル配列（"00:00" ~ "24:00"）と、
 *   数値をゼロパディングする関数を提供する。
 *   ホーム画面のガントチャート表示で使われる。
 */

// --- ユーティリティ関数 ---

/**
 * 数値を2桁のゼロパディング文字列に変換する
 * @param n - 変換したい数値（例: 9）
 * @returns ゼロパディングされた文字列（例: "09"）
 *
 * padStart(2, "0") は文字列メソッドで、文字列が2文字未満なら先頭に"0"を足す。
 * 例: "9".padStart(2, "0") → "09", "12".padStart(2, "0") → "12"
 */
export const pad = (n: number): string => n.toString().padStart(2, "0");

// --- 時間配列の生成 ---

/**
 * 00:00 ~ 24:00 までの30分刻みの時間ラベル配列。
 * ガントチャートの行ラベルや、時間範囲の判定に使われる。
 *
 * string[] は「文字列の配列」を意味する型注釈。
 * TypeScriptでは変数宣言時に型を明示できる（型推論で省略も可能）。
 */
export const allTimes: string[] = [];
// forループで0時〜23時まで回し、各時間の :00 と :30 を追加する
for (let h = 0; h < 24; h++) {
  allTimes.push(`${pad(h)}:00`); // 例: "00:00", "01:00", ..., "23:00"
  allTimes.push(`${pad(h)}:30`); // 例: "00:30", "01:30", ..., "23:30"
}
allTimes.push("24:00"); // 終了時刻として24:00を追加（最後のスロットの終了を表す）
